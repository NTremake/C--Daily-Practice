int main()//整形提升
{
	char a = 3;//char一个字节八个bit位，所以只截取3的补码的前8位，00000011
	char b = 127;//01111111
	char c = a + b;//a+b时，看第一位时0还是1，按第一位补齐32位；所以相加为00000000000000000000000010000010；然后c又是char，所以截取前8位10000010
	printf("%d\n", c);//打印十进制整数，又补齐32位补码，11111111111111111111111110000010；减一为反码11111111111111111111111110000001；原码为10000000000000000000000001111110；首位为1，负数，所以c=-126
	return 0;
}
int main()//计算一个数是二进制时。有多少个1
{
	int a = 0;
	int i = 0;
	int count = 0;
	scanf_s("%d", &a);
	for (i = 0; i < 32; i++)
	{
		if(((a >> i) & 1) == 1)//右移一位，查看个位是否为1
		{
			count++;
		}
	}
	printf("%d", count);
}

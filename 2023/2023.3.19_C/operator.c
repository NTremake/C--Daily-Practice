int main()
 {
	int a = 0;
	int b = 0;
	int max = 0;
	scanf_s("%d%d", &a, &b);
	max = (a > b ? a : b);//条件操作符（三目运算符），a大于b则为真，输出a，否则输出b
	printf("Max=%d\n", max);
	return 0;
  }
